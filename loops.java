class loops{
    public static void main (String args[]){
        int i,j=1,k=1;
       for( i=1;i<=10;i++){
        System.out.println(i+" ");
       }

       while(j<=10){
        System.out.println(j+" ");
        j++;
       }

       do{
         System.out.println(k+" ");
        k++;
       }while(k<=10);
    }
}